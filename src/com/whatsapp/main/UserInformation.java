    public void showMessage(PanelPopupMessage.MessageType messageType, String message) {
        PanelPopupMessage ms = new PanelPopupMessage();
        ms.showMessage(messageType, message);
        ms.repaint();

        ms.setBounds(jPanel1.getWidth() / 2 - ms.getPreferredSize().width / 2, -30, ms.getPreferredSize().width, ms.getPreferredSize().height);

        jPanel1.add(ms, 0);
        ms.setVisible(true);
        jPanel1.revalidate();
        jPanel1.repaint();

        TimingTarget target = new TimingTargetAdapter() {
            @Override
            public void timingEvent(float fraction) {
                int targetY;
                if (ms.getShow()) {
                    targetY = (int) (10 * (1f - fraction));
                } else {
                    targetY = (int) (10 * fraction);
                }

                ms.setBounds(jPanel1.getWidth() / 2 - ms.getPreferredSize().width / 2, targetY, ms.getPreferredSize().width, ms.getPreferredSize().height);
                jPanel1.revalidate();
                jPanel1.repaint();
            }

            @Override
            public void end() {
                if (ms.getShow()) {
                    ms.setVisible(false);
                    jPanel1.remove(ms);
                    jPanel1.revalidate();
                    jPanel1.repaint();
                    ms.setShow(false);
                } else {
                    ms.setShow(true);
                }
            }
        };

        Animator animator = new Animator(400, target);
        animator.setResolution(0);
        animator.setAcceleration(0.5f);
        animator.setDeceleration(0.5f);
        animator.start();

        Timer timer = new Timer(1500, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                ms.setVisible(false);
                ms.setShow(false);
                jPanel1.remove(ms);
                jPanel1.revalidate();
                jPanel1.repaint();
            }
        });
        timer.setRepeats(false);
        timer.start();
    }

