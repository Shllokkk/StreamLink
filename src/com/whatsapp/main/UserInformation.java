    private void setUserData() {
        String sql = "SELECT * FROM users WHERE Username = ?";
        
        try {
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1, superuser);
            ResultSet rs = ps.executeQuery();
            if(rs.next()) {
                txtUsername.setText(rs.getString("Username"));
                txtEmail.setText(rs.getString("Email"));
                txtDoj.setText(rs.getString("Joining_date"));
                txtPassword.setText(rs.getString("Password"));
                
                imageData = rs.getBytes("Image");
                BufferedImage img = ImageIO.read(new ByteArrayInputStream(imageData));
                lblImage.setIcon(new ImageIcon(img));
            }
        } catch(SQLException | IOException ex){
            showMessage(PanelPopupMessage.MessageType.ERROR, ex.getMessage());
        }
    }
    
    private void updateDb(String username, String email) {
        String sql = "UPDATE users SET Username = ?, Email = ?, Image = ? WHERE Username = ? ";
        try {
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1, username);
            ps.setString(2, email);
            ps.setBytes(3, imageData);
            ps.setString(4, superuser);
            ps.execute();
            
            PanelLoginAndRegister.superUsername = username;
            showMessage(PanelPopupMessage.MessageType.SUCCESS, "Data updated successfully!");
        } catch(SQLException ex) {
            showMessage(PanelPopupMessage.MessageType.ERROR, ex.getMessage());
        }
    }

